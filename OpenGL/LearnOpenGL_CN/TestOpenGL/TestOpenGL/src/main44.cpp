// MARK: - 平行光
/**
 当一个光源处于很远的地方时，来自光源的每条光线就会近似于互相平行。不论物体和/或者观察者的位置，看起来好像所有的光都来自于同一个方向。
 当我们使用一个假设光源处于无限远处的模型时，它就被称为定向光，因为它的所有光线都有着相同的方向，它与光源的位置是没有关系的。
 定向光非常好的一个例子就是太阳。太阳距离我们并不是无限远，但它已经远到在光照计算中可以把它视为无限远了。所以来自太阳的所有光线将被模拟为平行光线
 因为所有的光线都是平行的，所以物体与光源的相对位置是不重要的，因为对场景中每一个物体光的方向都是一致的。由于光的位置向量保持一致，场景中每个物体的光照计算将会是类似的。
 
 我们可以定义一个光线方向向量而不是位置向量来模拟一个定向光。着色器的计算基本保持不变，但这次我们将直接使用光的direction向量而不是通过position来计算lightDir向量。
 struct Light {
     // vec3 position; // 使用定向光就不再需要了
     vec3 direction;

     vec3 ambient;
     vec3 diffuse;
     vec3 specular;
 };
 ...
 void main()
 {
   vec3 lightDir = normalize(-light.direction);
   ...
 }
 
 我们首先对light.direction向量取反。我们目前使用的光照计算需求一个从片段至光源的光线方向，但人们更习惯定义定向光为一个从光源出发的全局方向。
 所以我们需要对全局光照方向向量取反来改变它的方向，它现在是一个指向光源的方向向量了。
 而且，记得对向量进行标准化，假设输入向量为一个单位向量是很不明智的。
 最终的lightDir向量将和以前一样用在漫反射和镜面光计算中。
 
 定向光对多个物体具有相同的影响
 对每个箱子都生成了一个不同的模型矩阵，每个模型矩阵都包含了对应的局部-世界坐标变换：

 for(unsigned int i = 0; i < 10; i++)
 {
     glm::mat4 model;
     model = glm::translate(model, cubePositions[i]);
     float angle = 20.0f * i;
     model = glm::rotate(model, glm::radians(angle), glm::vec3(1.0f, 0.3f, 0.5f));
     lightingShader.setMat4("model", model);

     glDrawArrays(GL_TRIANGLES, 0, 36);
 }
 
 同时，不要忘记定义光源的方向（注意我们将方向定义为从光源出发的方向，你可以很容易看到光的方向朝下）。
 lightingShader.setVec3("light.direction", -0.2f, -1.0f, -0.3f);
 
 我们一直将光的位置和位置向量定义为vec3，但一些人会喜欢将所有的向量都定义为vec4。当我们将位置向量定义为一个vec4时，很重要的一点是要将w分量设置为1.0，这样变换和投影才能正确应用。
 然而，当我们定义一个方向向量为vec4的时候，我们不想让位移有任何的效果（因为它仅仅代表的是方向），所以我们将w分量设置为0.0。
 方向向量就会像这样来表示：vec4(0.2f, 1.0f, 0.3f, 0.0f)。这也可以作为一个快速检测光照类型的工具：你可以检测w分量是否等于1.0，来检测它是否是光的位置向量；w分量等于0.0，则它是光的方向向量，这样就能根据这个来调整光照计算了：
 if(lightVector.w == 0.0) // 注意浮点数据类型的误差
   // 执行定向光照计算
 else if(lightVector.w == 1.0)
   // 根据光源的位置做光照计算
 
 这正是旧OpenGL（固定函数式）决定光源是定向光还是位置光源(Positional Light Source)的方法，并根据它来调整光照。
 */

